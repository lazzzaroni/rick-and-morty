import ApiService from "@/api/apiClient";
import AllCards from "@/components/AllCards";
import Head from "next/head";
import { useQuery } from "react-query";
import { type ApiData } from "types";

export default function Home() {
  const { data, isLoading, isError, isSuccess } = useQuery<ApiData, Error>(
    ["getCharacters"],
    async () => {
      return await ApiService.getAll();
    }
  );

  const renderResult = () => {
    if (isLoading) {
      return <div>Loading...</div>;
    }
    if (isError) {
      return <div>Something went wrong</div>;
    }
    if (isSuccess) {
      return <AllCards data={data} />;
    }
    return null;
  };

  return (
    <>
      <Head>
        <title>Rick and Morty</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="container m-auto grid w-full grid-cols-1 gap-8 p-8 sm:grid-cols-2 md:grid-cols-4">
        {renderResult()}
      </div>
    </>
  );
}
