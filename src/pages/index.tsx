import ApiService from "@/api/apiClient";
import AllCards from "@/components/AllCards";
import Head from "next/head";
import { useRouter } from "next/router";
import { useEffect, useState } from "react";
import ReactPaginate from "react-paginate";
import { useQuery } from "react-query";
import { type ApiData } from "types";

export default function Home() {
  const router = useRouter();
  const [page, setPage] = useState(0);

  const { data, error, isLoading, isError, isSuccess } = useQuery<
    ApiData,
    Error
  >(
    ["getCharacters", page],
    async () => {
      return await ApiService.getAll(page);
    },
    {
      keepPreviousData: true,
    }
  );

  const renderResult = () => {
    if (isLoading) {
      return <div>Loading...</div>;
    }
    if (isError) {
      return <div>Error: {error.message}</div>;
    }
    if (isSuccess) {
      return <AllCards data={data} />;
    }
    return null;
  };

  useEffect(() => {
    if (router.query.page) {
      setPage(router.query.page as unknown as number);
    }
  }, [router.query.page]);

  const handlePageClick = ({ selected }: { selected: number }) => {
    setPage(selected++);
    void router.push(`?page=${selected}`, undefined, { shallow: true });
  };

  return (
    <>
      <Head>
        <title>Rick and Morty</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <ReactPaginate
        previousLabel="←"
        nextLabel="→"
        previousClassName="py-0 px-1 leading-6 rounded border text-xs bg-gray-200"
        previousLinkClassName="aria-disabled:text-gray-400/50"
        nextClassName="py-0 px-1 leading-6 bg-gray-200 rounded border text-xs"
        nextLinkClassName="aria-disabled:text-gray-300"
        pageClassName="py-0 px-1 leading-6 bg-gray-200 rounded border text-xs"
        breakClassName="py-0 px-1 leading-6 rounded text-xs"
        containerClassName="flex gap-0.5"
        activeClassName="bg-gray-300"
        forcePage={page - 1}
        pageCount={data?.info.pages as number}
        onPageChange={handlePageClick}
      />
      <div className="container mb-auto grid w-full grid-cols-1 gap-8 p-8 sm:grid-cols-2 md:grid-cols-4">
        {renderResult()}
      </div>
      <ReactPaginate
        previousLabel="←"
        nextLabel="→"
        previousClassName="py-0 px-1 leading-6 rounded border text-xs bg-gray-200"
        previousLinkClassName="aria-disabled:text-gray-400/50"
        nextClassName="py-0 px-1 leading-6 bg-gray-200 rounded border text-xs"
        nextLinkClassName="aria-disabled:text-gray-300"
        pageClassName="py-0 px-1 leading-6 bg-gray-200 rounded border text-xs"
        breakClassName="py-0 px-1 leading-6 rounded text-xs"
        containerClassName="flex gap-0.5 mb-8"
        activeClassName="bg-gray-300"
        forcePage={page - 1}
        pageCount={data?.info.pages as number}
        onPageChange={handlePageClick}
      />
    </>
  );
}
